    public void ViewData(){
        
        try {
            String query = "SELECT * FROM Stores Left Outer Join Has On Stores.Store_ID= Has.Store_ID Left Outer Join Citizens On Has.NID_No=Citizens.NID_No";
            PreparedStatement ps = connection.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            String area,storeName,oName;
            int storeID;
            DefaultTableModel model = (DefaultTableModel)jTableStore.getModel();
            model.setRowCount(0);
            
            while(rs.next()){
                
                storeName = rs.getString("Store_Name");
                storeID = rs.getInt("Store_ID");
                area = rs.getString("Area");
                oName = rs.getString("Name");
                
                
                
                Object[] col = new Object[4];
                
                col[0] = storeName;
                col[1] = storeID;
                col[2] = oName;
                col[3] = area;
                
            
                model.addRow(col);
            }
        } catch (SQLException ex) {
            Logger.getLogger(House.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
